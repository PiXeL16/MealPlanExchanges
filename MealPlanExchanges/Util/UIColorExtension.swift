import UIKit
import DynamicColor

// MARK: - App Colors
extension UIColor {
    
    /**
     Gets the pink color used in the heart app
     
     - returns: <#return value description#>
     */
    public class func heartPink() -> UIColor{
        
        return UIColor(hexString: "#E55D89");
    }

}
